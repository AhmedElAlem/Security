const root = document.documentElement;
const langBtn = document.getElementById('langToggle');
let currentLang = 'en';
function setLang(lang){
  currentLang = lang;
  const dict = translations[lang];
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.body.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key = el.dataset.i18n;
    if(dict[key]) el.textContent = dict[key];
  });
  langBtn.textContent = lang === 'ar' ? 'EN' : 'AR';
}
langBtn?.addEventListener('click',()=> setLang(currentLang === 'en' ? 'ar' : 'en'));

const slides = Array.from(document.querySelectorAll('.hero-bg'));
const lines = document.getElementById('slideLines');
let slideIndex = 0;
let slideTimer;
function buildLines(){
  slides.forEach((_,i)=>{
    const b = document.createElement('button');
    b.type='button';
    b.setAttribute('aria-label',`Go to slide ${i+1}`);
    b.addEventListener('click',()=>goSlide(i,true));
    lines.appendChild(b);
  });
}
function goSlide(i, manual=false){
  slideIndex = (i + slides.length) % slides.length;
  slides.forEach((s,idx)=>s.classList.toggle('active', idx === slideIndex));
  lines.querySelectorAll('button').forEach((b,idx)=>b.classList.toggle('active', idx === slideIndex));
  if(manual){ clearInterval(slideTimer); slideTimer = setInterval(()=>goSlide(slideIndex+1), 5500); }
}
buildLines();
goSlide(0);
document.getElementById('nextSlide')?.addEventListener('click',()=>goSlide(slideIndex+1,true));
document.getElementById('prevSlide')?.addEventListener('click',()=>goSlide(slideIndex-1,true));
slideTimer = setInterval(()=>goSlide(slideIndex+1), 5500);

const revealObserver = new IntersectionObserver((entries)=>{
  entries.forEach(entry=>{ if(entry.isIntersecting){ entry.target.classList.add('visible'); revealObserver.unobserve(entry.target); } });
},{threshold:.16});
document.querySelectorAll('.reveal').forEach(el=>revealObserver.observe(el));

document.getElementById('backTop')?.addEventListener('click',(e)=>{
  e.preventDefault();
  window.scrollTo({top:0,behavior:'smooth'});
});

